class EntradaTP:
    def __init__(self, bitP, bitM, bitU, endQuadroMP):
        self.bitP = bitP
        self.bitM = bitM
        self.bitU = bitU
        self.endQuadroMP = endQuadroMP


    def getBitP(self):
        return self.bitP
    
    def getBitM(self):
        return self.bitM
    
    def getBitU(self):
        return self.bitU
    
    def getEndQuadroMP(self):
        return self.endQuadroMP
